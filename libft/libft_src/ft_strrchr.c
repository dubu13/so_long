#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (i > 0)
	{
		if (s[i - 1] == (char)c)
		{
			return ((char *)(s + i -1));
		}
		i--;
	}
	if ((char)c == 0)
	{
		return ((char *)(s + ft_strlen(s)));
	}
	return (NULL);
}
