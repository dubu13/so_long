#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*arr;

	arr = NULL;
	if (count > 2147483647 || size > 2147483647)
		return (NULL);
	arr = malloc(count * size);
	if (arr)
		ft_bzero(arr, count * size);
	return (arr);
}
