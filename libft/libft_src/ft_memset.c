#include "libft.h"

void	*ft_memset(void *str, int c, size_t len)
{
	size_t			i;
	unsigned char	*str2;

	str2 = (unsigned char *) str;
	i = 0;
	while (len > i)
	{
		str2[i] = (unsigned char)c;
		i++;
	}
	return (str);
}
