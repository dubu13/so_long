#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	int		s;

	i = 0;
	if (dstsize != 0)
	{
		while ((dstsize - 1) > i && src[i] != '\0')
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	s = ft_strlen(src);
	return (s);
}
